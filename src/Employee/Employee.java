package Employee;

/**
 *
 * @author bellogbemisola
 */
public class Employee {   //EXAMPLE OF INHERITANCE
    private String number;
    private String name;

    public Employee(String num, String nam) {
        number = num;
        name = nam;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
